# include "3-calc.h"

/**
* op_add - Function for addition
* @a: The first number
* @b: The second number
* Return: The function result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Function for subtraction
* @a: The first number
* @b: The second number
* Return: The function result
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - Function for multiplication
* @a: The first number
* @b: The second number
* Return: The function result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Function for division
* @a: The first number
* @b: The second number
* Return: The function result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Function for modular
* @a: The first number
* @b: The second number
* Return: The function result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
